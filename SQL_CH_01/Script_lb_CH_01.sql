--1. Выручка по категориям
SELECT
    category,
    sum(quantity * unit_price * (1 - discount_pct)) AS revenue
FROM sales_var001
GROUP BY category
ORDER BY revenue DESC;
--==================================================================
--2. Топ-3 клиентов
SELECT
    customer_id,
    count() AS purchases,
    sum(quantity) AS total_items
FROM sales_var001
GROUP BY customer_id
ORDER BY purchases DESC
LIMIT 3;
--==================================================================
--3. Средний чек по месяцам
SELECT
    toYYYYMM(sale_timestamp) AS month,
    avg(quantity * unit_price) AS avg_check
FROM sales_var001
GROUP BY month
ORDER BY month;
--==================================================================
--4. Фильтр по месяцу
SELECT *
FROM sales_var001
WHERE sale_timestamp >= '2024-06-01'
  AND sale_timestamp < '2024-07-01';
